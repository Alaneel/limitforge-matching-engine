import assert from "node:assert/strict";
import test from "node:test";

async function render() {
  const workerUrl = new URL("../dist/server/index.js", import.meta.url);
  workerUrl.searchParams.set("test", `${process.pid}-${Date.now()}`);
  const { default: worker } = await import(workerUrl.href);

  return worker.fetch(
    new Request("http://localhost/", {
      headers: { accept: "text/html" },
    }),
    {
      ASSETS: {
        fetch: async () => new Response("Not found", { status: 404 }),
      },
    },
    {
      waitUntil() {},
      passThroughOnException() {},
    },
  );
}

test("server-renders the LimitForge dashboard", async () => {
  const response = await render();
  assert.equal(response.status, 200);
  assert.match(response.headers.get("content-type") ?? "", /^text\/html\b/i);

  const html = await response.text();
  assert.match(html, /<title>LimitForge — Matching Engine Dashboard<\/title>/i);
  assert.match(html, /LimitForge/);
  assert.match(html, /Java order matching engine/);
  assert.match(html, /Order book/);
  assert.match(html, /Recent executions/);
  assert.match(html, /Client positions/);
  assert.doesNotMatch(html, /site-creator-vinext-starter|react-loading-skeleton/);
});
